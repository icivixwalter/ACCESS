NOTE
	la query che accoda i valori delle pagine MASTER in NELLA TABELLA TMP




SCHEMA

		INSERT INTO 
				MSys_TB0402_VISUAL_PAG_MASTER_TMP ( NRO_OGGETTO_i, TIPOGGETTO_s, COD_FORMS_s, NOMEOGGETTO_s, NOMEpagina_s, VISIBLE_b, NOTA_OGGETTO_s, NOTEex_s, COD_PROGETTO_s, COD_PROGETTO_s )

			SELECT 
					MSys_TB0402_VISUAL_PAG_MASTER.NRO_OGGETTO_i, 
					MSys_TB0402_VISUAL_PAG_MASTER.TIPOGGETTO_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.COD_FORMS_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.NOMEOGGETTO_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.NOMEpagina_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.VISIBLE_b, 
					MSys_TB0402_VISUAL_PAG_MASTER.NOTA_OGGETTO_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.NOTEex_s, 
					MSys_TB0402_VISUAL_PAG_MASTER.COD_PROGETTO_s, 
					MSys_TB0402_VISUAL_PAG_MASTER_TMP.COD_PROGETTO_s

					FROM 
					(MSys_TB0401_GE_TabContr_MASTER 
					INNER JOIN 
					MSys_TB0402_VISUAL_PAG_MASTER 
					ON 
					MSys_TB0401_GE_TabContr_MASTER.COD_PROGETTO_s = MSys_TB0402_VISUAL_PAG_MASTER.COD_PROGETTO_s) 	
					LEFT JOIN 
					MSys_TB0402_VISUAL_PAG_MASTER_TMP ON 
						MSys_TB0402_VISUAL_PAG_MASTER.COD_PROGETTO_s = MSys_TB0402_VISUAL_PAG_MASTER_TMP.COD_PROGETTO_s

					WHERE 
					(((MSys_TB0402_VISUAL_PAG_MASTER_TMP.COD_PROGETTO_s) Is Null))
					WITH OWNERACCESS OPTION;
